def soma():
    """
    A função soma os valores 10 e 5.
    """
    
    print(10+5)
def soma2(x,y):
    """
    A função recebe dois valores e soma esses repetivos valores.

    x,y: dois valores numericos
    Exemplo:
    soma2(10,5)
    """

    print(x+y)
def main():
    soma()
